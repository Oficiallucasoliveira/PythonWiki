def main():
	p1 = int(input('Digite a soma para exibir Tabela Tabuada: '))
	while True:
				

		for i in range(1,11):
				
			somar = p1 * i

			print('Numero: ', p1, '* ', i, ' = ', somar)


		break	
main()
	